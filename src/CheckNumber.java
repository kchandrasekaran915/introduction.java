public class CheckNumber {
    public static void main(String[] args) {
        int number = 123;  // Given input

        if (number > 0) {
            System.out.println("Output = Positive");
        } else if (number < 0) {
            System.out.println("Output = Negative");
        } else {
            System.out.println("Output = Zero");
        }
    }
}


